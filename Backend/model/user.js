class user {
    constructor(
      id,
      firstName,
      lastName,
      country,
      phoneNumber,
      dateOfBirth,
      gender,
      email,
      password,
    ) {
      this.id = id;
      this.firstName = firstName;
      this.lastName = lastName;
      this.country = country;
      this.phoneNumber = phoneNumber;
      this.dateOfBirth = dateOfBirth;
      this.gender = gender;
      this.email = email;
      this.password = password;
    }
  }
  
  module.exports = user;
  